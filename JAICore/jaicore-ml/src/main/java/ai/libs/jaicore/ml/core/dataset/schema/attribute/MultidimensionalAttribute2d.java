package ai.libs.jaicore.ml.core.dataset.schema.attribute;

import org.api4.java.ai.ml.core.dataset.schema.attribute.IAttributeValue;

/**
 * Attribute for 2dDouble Arrays
 *
 * @author Lukas Fehring
 *
 */
public class MultidimensionalAttribute2d extends MultidimensionalAttribute<double[][]> {

	private static final long serialVersionUID = -3300254871190010390L;
	private int xsize;
	private int ysize;

	public int getXsize() {
		return this.xsize;
	}

	public int getYsize() {
		return this.ysize;
	}

	/**
	 * Attribute Constructor
	 *
	 * @param name attribute name
	 * @param xsize of AttributeValues generated by this Attribute
	 * @param ysize of AttributeValues generated by this Attribute
	 */
	public MultidimensionalAttribute2d(final String name, final int xsize, final int ysize) {
		super(name);
		this.xsize = xsize;
		this.ysize = ysize;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = super.hashCode();
		result = prime * result + this.xsize;
		result = prime * result + this.ysize;
		return result;
	}

	@Override
	public boolean equals(final Object obj) {
		if (this == obj) {
			return true;
		}
		if (!super.equals(obj)) {
			return false;
		}
		if (this.getClass() != obj.getClass()) {
			return false;
		}
		MultidimensionalAttribute2d other = (MultidimensionalAttribute2d) obj;
		if (this.xsize != other.xsize) {
			return false;
		}
		if (this.ysize != other.ysize) {
			return false;
		}
		return true;
	}

	/**
	 * {@inheritDoc} takes object type double[][] or MultidimensionalAttributeValue - parsed to MultidimensionalAttributeValue
	 */
	@Override
	public IAttributeValue getAsAttributeValue(final Object object) {
		if (object instanceof double[][]) {
			return new MultidimensionalAttributeValue2d(this, (double[][]) object);
		} else if (object instanceof MultidimensionalAttributeValue2d) {
			return new MultidimensionalAttributeValue2d(this, ((MultidimensionalAttributeValue2d) object).getValue());
		}

		throw new IllegalArgumentException("No valid value for this attribute");

	}

	@Override
	public boolean isValidValue(final Object value) {
		return (value instanceof MultidimensionalAttributeValue2d || value instanceof double[][]);
	}

	@Override
	public String getStringDescriptionOfDomain() {
		return "[MDA2] " + this.getName();
	}

	/**
	 * {@inheritDoc} parses String string to MutidimensionalAttributeValue
	 */
	@Override
	public double[][] deserializeAttributeValue(final String string) {
		String arraystring = string.replaceAll(this.SINGLE_SPACE, this.ARRAY_STRING_SPLITTER);
		String[] test = arraystring.split(this.INNTER_ARRAY_SPLITTER);
		test[0] = test[0].substring(2);
		test[test.length - 1] = test[test.length - 1].substring(0, test[test.length - 1].length() - 2);
		double[][] values = new double[this.xsize][this.ysize];
		for (int i = 0; i < test.length; i++) {
			String[] tmp = test[i].split(this.ARRAY_STRING_SPLITTER);
			for (int j = 0; j < tmp.length; j++) {
				values[i][j] = Double.parseDouble(tmp[j]);
			}
		}
		return values;
	}

}
